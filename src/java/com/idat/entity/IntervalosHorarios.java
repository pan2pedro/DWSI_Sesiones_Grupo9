
package com.idat.entity;

public class IntervalosHorarios {
private int idIntervaloHorario;
    private String intervaloHorario;
    private int numeroVehiculos;

    // Getters y Setters
    public int getIdIntervaloHorario() {
        return idIntervaloHorario;
    }

    public void setIdIntervaloHorario(int idIntervaloHorario) {
        this.idIntervaloHorario = idIntervaloHorario;
    }

    public String getIntervaloHorario() {
        return intervaloHorario;
    }

    public void setIntervaloHorario(String intervaloHorario) {
        this.intervaloHorario = intervaloHorario;
    }

    public int getNumeroVehiculos() {
        return numeroVehiculos;
    }

    public void setNumeroVehiculos(int numeroVehiculos) {
        this.numeroVehiculos = numeroVehiculos;
    }
}
